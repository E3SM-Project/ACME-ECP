      precflux(k) = precflux(k) * factor_xy*dz/dt/nstop  !kg/m2/dz in M2005 -->kg/m2/s or mm/s (idt_gl=1/dt/nstop)

      l = plev-k+1
      crm_output%flux_u    (icrm,l) = (uwle(k) + uwsb(k))*tmp1*factor_xy/nstop
      crm_output%flux_v    (icrm,l) = (vwle(k) + vwsb(k))*tmp1*factor_xy/nstop
#ifdef sam1mom
      crm_output%flux_qt   (icrm,l) = mkwle(k,1) + mkwsb(k,1)
      crm_output%fluxsgs_qt(icrm,l) = mkwsb(k,1)
      crm_output%flux_qp   (icrm,l) = mkwle(k,2) + mkwsb(k,2)
      crm_output%qt_trans  (icrm,l) = mkadv(k,1) + mkdiff(k,1)
      crm_output%qp_trans  (icrm,l) = mkadv(k,2) + mkdiff(k,2)
#endif /* sam1mom */
#ifdef m2005
      crm_output%flux_qt   (icrm,l) = mkwle(k,1   ) + mkwsb(k,1   ) +  &
                         mkwle(k,iqci) + mkwsb(k,iqci)
      crm_output%fluxsgs_qt(icrm,l) = mkwsb(k,1   ) + mkwsb(k,iqci)
      crm_output%flux_qp   (icrm,l) = mkwle(k,iqr) + mkwsb(k,iqr) +  &
                         mkwle(k,iqs) + mkwsb(k,iqs) + mkwle(k,iqg) + mkwsb(k,iqg)
      crm_output%qt_trans  (icrm,l) = mkadv (k,1) + mkadv (k,iqci) + &
                         mkdiff(k,1) + mkdiff(k,iqci)
      crm_output%qp_trans  (icrm,l) = mkadv (k,iqr) + mkadv (k,iqs) + mkadv (k,iqg) + &
                         mkdiff(k,iqr) + mkdiff(k,iqs) + mkdiff(k,iqg)
#endif /* m2005 */
      crm_output%tkesgsz   (icrm,l)= rho(k)*sum(tke(1:nx,1:ny,k))*factor_xy
      crm_output%tkez      (icrm,l)= rho(k)*0.5*(u2z+v2z*YES3D+w2z)*factor_xy + crm_output%tkesgsz(icrm,l)
      crm_output%tkz       (icrm,l) = sum(tk(1:nx, 1:ny, k)) * factor_xy
      crm_output%precflux      (icrm,l) = precflux(k)/1000.       !mm/s  -->m/s

      crm_output%qp_fall   (icrm,l) = qpfall(k)
      crm_output%qp_evp    (icrm,l) = qpevp(k)
      crm_output%qp_src    (icrm,l) = qpsrc(k)

      crm_output%qt_ls     (icrm,l) = qtend(k)
      crm_output%t_ls      (icrm,l) = ttend(k)
    enddo

#ifdef ECPP
    abnd         (icrm,:,:,:,:)=0.0
    abnd_tf      (icrm,:,:,:,:)=0.0
    massflxbnd   (icrm,:,:,:,:)=0.0
    acen         (icrm,:,:,:,:)=0.0
    acen_tf      (icrm,:,:,:,:)=0.0
    rhcen        (icrm,:,:,:,:)=0.0
    qcloudcen    (icrm,:,:,:,:)=0.0
    qicecen      (icrm,:,:,:,:)=0.0
    qlsinkcen    (icrm,:,:,:,:)=0.0
    precrcen     (icrm,:,:,:,:)=0.0
    precsolidcen (icrm,:,:,:,:)=0.0
    qlsink_bfcen (icrm,:,:,:,:)=0.0
    qlsink_avgcen(icrm,:,:,:,:)=0.0
    praincen     (icrm,:,:,:,:)=0.0

    wupthresh_bnd   (icrm,:)=0.0
    wdownthresh_bnd (icrm,:)=0.0
    wwqui_cen       (icrm,:)=0.0
    wwqui_bnd       (icrm,:)=0.0
    wwqui_cloudy_cen(icrm,:)=0.0
    wwqui_cloudy_bnd(icrm,:)=0.0

    ! default is clear, non-precipitating, and quiescent class
    abnd   (icrm,:,1,1,1)=1.0
    abnd_tf(icrm,:,1,1,1)=1.0
    acen   (icrm,:,1,1,1)=1.0
    acen_tf(icrm,:,1,1,1)=1.0
    do k=1, nzm
      l=plev-k+1
      acen            (icrm,l,:,:,:) = area_cen_sum        (k,:,1:ncls_ecpp_in,:)
      acen_tf         (icrm,l,:,:,:) = area_cen_final      (k,:,1:ncls_ecpp_in,:)
      rhcen           (icrm,l,:,:,:) = rh_cen_sum          (k,:,1:ncls_ecpp_in,:)
      qcloudcen       (icrm,l,:,:,:) = qcloud_cen_sum      (k,:,1:ncls_ecpp_in,:)
      qicecen         (icrm,l,:,:,:) = qice_cen_sum        (k,:,1:ncls_ecpp_in,:)
      qlsinkcen       (icrm,l,:,:,:) = qlsink_cen_sum      (k,:,1:ncls_ecpp_in,:)
      precrcen        (icrm,l,:,:,:) = precr_cen_sum       (k,:,1:ncls_ecpp_in,:)
      precsolidcen    (icrm,l,:,:,:) = precsolid_cen_sum   (k,:,1:ncls_ecpp_in,:)
      wwqui_cen       (icrm,l)       = wwqui_cen_sum       (k)
      wwqui_cloudy_cen(icrm,l)       = wwqui_cloudy_cen_sum(k)
      qlsink_bfcen    (icrm,l,:,:,:) = qlsink_bf_cen_sum   (k,:,1:ncls_ecpp_in,:)
      qlsink_avgcen   (icrm,l,:,:,:) = qlsink_avg_cen_sum  (k,:,1:ncls_ecpp_in,:)
      praincen        (icrm,l,:,:,:) = prain_cen_sum       (k,:,1:ncls_ecpp_in,:)
    enddo
    do k=1, nzm+1
      l=plev+1-k+1
      abnd            (icrm,l,:,:,:) = area_bnd_sum        (k,:,1:ncls_ecpp_in,:)
      abnd_tf         (icrm,l,:,:,:) = area_bnd_final      (k,:,1:ncls_ecpp_in,:)
      massflxbnd      (icrm,l,:,:,:) = mass_bnd_sum        (k,:,1:ncls_ecpp_in,:)
      wupthresh_bnd   (icrm,l)       = wup_thresh          (k)
      wdownthresh_bnd (icrm,l)       = wdown_thresh        (k)
      wwqui_bnd       (icrm,l)       = wwqui_bnd_sum       (k)
      wwqui_cloudy_bnd(icrm,l)       = wwqui_cloudy_bnd_sum(k)
    enddo
#endif /* ECPP */

    crm_output%timing_factor(icrm) = crm_output%timing_factor(icrm) / nstop

#ifdef CLUBB_CRM
    ! Deallocate CLUBB variables, etc.
    ! -UWM
    if ( doclubb .or. doclubbnoninter ) call clubb_sgs_cleanup( )
#endif
#ifdef ECPP
    ! Deallocate ECPP variables
    call ecpp_crm_cleanup ()
#endif

  enddo

  call deallocate_grid()
  call deallocate_tracers()
  call deallocate_sgs()
  call deallocate_vars()
#ifdef sam1mom
  call deallocate_micro_params()
  call deallocate_micro()
#endif
#if defined( SP_ESMT )
  call deallocate_scalar_momentum()
#endif

end subroutine crm

end module crm_module
